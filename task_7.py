a = int(input('Введите коэффициент а: '))
b = int(input('Введите коэффициент b: '))
c = int(input('Введите коэффициент с: '))

# i = (f'{a}x^2 + {b}x + {c}')
d = b**2 + 4*a*c

if d>0:
    x1 = (-b + d**0.5)/2*a
    x2 = (b + d**0.5)/2*a
    print(f'Первый корень равен {x1}')
    print(f'Второй корень равен {x2}')
elif d==0:
    x = -b/2*a
    print(f'Корень равен {x}')
else:
    print('Корней нет')